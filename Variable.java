package java0622;

public class Variable {
	public static void main(String[] args) {
		// 1 byte
		byte a = 0;
		// 2 byte
		char h = 'a';
		// 2 byte
		short b = 'i';
		// 4 byte
		int c = 99999;
		// 8 byte
		long d = 3222l;
		// 4 byte
		float e = 335.15f;
		// 8 byte
		double f = 320.4565d;

		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);
		System.out.println(h);
	}
}
