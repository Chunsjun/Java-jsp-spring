package org.bytal.domain;

import java.io.Serializable;
import java.sql.Date;

public class Rent implements Serializable {
	private int rentNo;
	private String memberNo;
	private int bicycleNo;
	private String rentDate;
	private Date returnDate;
	private String userStatus;

	public Rent() {

	}

	public Rent(int rentNo, String memberNo, int bicycleNo, String rentDate,
			Date returnDate, String userStatus) {
		this.rentNo = rentNo;
		this.memberNo = memberNo;
		this.bicycleNo = bicycleNo;
		this.rentDate = rentDate;
		this.returnDate = returnDate;
		this.userStatus = userStatus;
	}

	public int getRentNo() {
		return rentNo;
	}

	public void setRentNo(int rentNo) {
		this.rentNo = rentNo;
	}

	public String getMemberNo() {
		return memberNo;
	}

	public void setMemberNo(String memberNo) {
		this.memberNo = memberNo;
	}

	public int getBicycleNo() {
		return bicycleNo;
	}

	public void setBicycleNo(int bicycleNo) {
		this.bicycleNo = bicycleNo;
	}

	public String getRentDate() {
		return rentDate;
	}

	public void setRentDate(String rentDate) {
		this.rentDate = rentDate;
	}

	public Date getReturnDate() {
		return returnDate;
	}

	public void setReturnDate(Date returnDate) {
		this.returnDate = returnDate;
	}

	public String getUserStatus() {
		return userStatus;
	}

	public void setUserStatus(String userStatus) {
		this.userStatus = userStatus;
	}
}